package org.example.chatapp.ENUM;

/**
 * FriendRequestStatus.java
 * Enum representing the status of a request.
 * Values:
 * - PENDING: The request was sent but not decided
 * - ACCEPTED: The request was accepted
 * - REJECTED: The request was rejected
 */

public enum FriendRequestStatus {
    PENDING,
    ACCEPTED,
    REJECTED
}
